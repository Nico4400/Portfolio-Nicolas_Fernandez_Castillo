const Experience = () => {
    // Estado para controlar qué tarjeta está expandida
    const [expandedCardId, setExpandedCardId] = useState(null);

    const toggleExpand = (id) => {
        setExpandedCardId(prevId => prevId === id ? null : id);
    };

    // Función para obtener el ícono genérico si no hay logo
    const getGenericIcon = (type) => {
        switch (type) {
            case "Trabajo":
                return <FaBriefcase />;
            case "Educación":
                return <FaGraduationCap />;
            case "Emprendimiento":
                return <FaBullhorn />; // FaLightbulb, FaRocket
            default:
                return <FaBriefcase />;
        }
    };

    return (
        <section className={styles.experienceSection}>
            <h2 className={styles.sectionTitle}>Mi Trayectoria</h2>
            <p className={styles.sectionSubtitle}>
                Un recorrido por mi experiencia profesional, académica y emprendedora.
            </p>

            <div className={styles.timelineContainer}>
                {experienceData.map((experience, index) => (
                    <div key={experience.id} className={`${styles.timelineItem} ${index % 2 === 0 ? styles.left : styles.right}`}>
                        {/* Icono/Logo en la línea de tiempo */}
                        <div className={styles.timelineIconContainer}>
                            {experience.logo ? (
                                <img src={experience.logo} alt={`Logo de ${experience.organization}`} className={styles.timelineLogo} />
                            ) : (
                                <span className={styles.timelineGenericIcon}>{getGenericIcon(experience.type)}</span>
                            )}
                        </div>

                        {/* Tarjeta de Experiencia */}
                        <div className={styles.timelineCard}>
                            <div className={styles.cardHeader}>
                                <h3 className={styles.cardTitle}>{experience.position}</h3>
                                <p className={styles.cardSubtitle}>{experience.organization} | {experience.period}</p>
                                <p className={styles.cardBrief}>{experience.brief}</p>
                                {experience.descriptionPoints && experience.descriptionPoints.length > 0 && (
                                    <button
                                        className={styles.expandButton}
                                        onClick={() => toggleExpand(experience.id)}
                                        aria-expanded={expandedCardId === experience.id}
                                        aria-controls={`details-${experience.id}`}
                                        aria-label={expandedCardId === experience.id ? "Contraer detalles" : "Expandir detalles"}
                                    >
                                        {expandedCardId === experience.id ? <FaMinus /> : <FaPlus />}
                                        <span className={styles.expandButtonText}>
                                            {expandedCardId === experience.id ? 'Ver menos' : 'Ver más'}
                                        </span>
                                    </button>
                                )}
                            </div>
                            {/* Detalles expandibles */}
                            <div
                                id={`details-${experience.id}`}
                                className={`${styles.cardDetails} ${expandedCardId === experience.id ? styles.expanded : ''}`}
                            >
                                <ul>
                                    {experience.descriptionPoints.map((point, i) => (
                                        <li key={i}>{point}</li>
                                    ))}
                                </ul>
                            </div>
                        </div>
                    </div>
                ))}
            </div>
        </section>
    );
};

export default Experience;